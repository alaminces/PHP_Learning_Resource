**2. Question: How do you concatenate two strings in PHP?

Answer: Using the . (dot) operator, like this: $str1 . $str2;
**3. Question: Which function is used to replace occurrences of a word or substring in a string?

Answer: str_replace()
**4. Question: What is the difference between single quotes and double quotes when declaring a string?

Answer: Single quotes treat the content as a literal string, while double quotes allow for variable interpolation and escape sequences.
**5. Question: How can you convert a string to lowercase in PHP?

Answer: Using strtolower() function.
**6. Question: Which PHP function is used to split a string into an array based on a delimiter?

Answer: explode()
**7. Question: How do you check if a string contains a specific substring in PHP?

Answer: You can use strpos() or strstr() functions.
**8. Question: What is the purpose of the trim() function in PHP?

Answer: trim() is used to remove whitespace or specified characters from the beginning and end of a string.
**9. Question: How can you reverse a string in PHP?

Answer: Using the strrev() function.
**10. Question: Which function is used to find the position of the last occurrence of a character in a string?
- Answer: strrpos()

**11. Question: How do you convert a string to uppercase in PHP?
- Answer: Using strtoupper() function.

**12. Question: What is the difference between strcmp() and strcasecmp()?
- Answer: strcmp() is case-sensitive, while strcasecmp() is case-insensitive when comparing two strings.

**13. Question: How can you check if a string starts with a specific substring?
- Answer: You can use substr() to check if a string starts with a specific substring.

**14. Question: Which function is used to count the number of occurrences of a character in a string?
- Answer: substr_count()

**15. Question: How do you remove whitespace from the beginning and end of a string without affecting spaces inside it?
- Answer: Use the trim() function.

**16. Question: What is the purpose of the str_pad() function in PHP?
- Answer: str_pad() is used to pad a string to a certain length with another string.

**17. Question: How can you convert a string to an array of characters in PHP?
- Answer: Using str_split() function.

**18. Question: How do you check if a string contains only letters or digits?
- Answer: Use ctype_alpha() to check for letters and ctype_digit() to check for digits.

**19. Question: Which PHP function is used to replace special characters with HTML entities to prevent XSS attacks?
- Answer: htmlspecialchars()

**20. Question: How can you extract a specific number of characters from the middle of a string in PHP?
- Answer: Use substr() with appropriate start and length parameters.


পিএইচপি-তে ফাইনাল কীওয়ার্ড কী কাজ করে?

উত্তর: ফাইনাল কীওয়ার্ডটি ব্যবহার করে আপনি একটি ক্লাস অথবা মেথডের উপর পরিবর্তন করতে পারবেন না। একটি ফাইনাল ক্লাস উপাদান বা মেথড সাবক্লাসে ইনহেরিট করা যাবে না।

পিএইচপি-তে স্ট্যাটিক কীওয়ার্ডের উদ্দেশ্য কী?

উত্তর: স্ট্যাটিক কীওয়ার্ডটি পিএইচপি-তে ক্লাস-লেভেল প্রোপার্টি এবং মেথড সংজ্ঞায়িত করতে ব্যবহার করা হয়, যা সংযোগিত ক্লাসের উদ্দেশ্যে না হয়, বরং স্ট্যাটিক ক্লাস নিজে ব্যবহার করে। এই প্রোপার্টি এবং মেথডগুলি ব্যক্তিগত ইনস্ট্যান্স নয়, ক্লাস স্তরে কাজ করে।

পিএইচপি-তে ভিসিবিলিটি (অ্যাক্সেস মডিফায়ার্স) কী?

উত্তর: পিএইচপি-তে ভিসিবিলিটি অক্ষেপকর করে একটি প্রোপার্টি বা মেথডের কোনও ক্লাসে কার্য করতে যে কোন অন্য ক্লাস অথবা অবজেক্টের কাছে এক্সেসের স্তর সেট করে। পিএইচপি-তে তিনটি প্রধান ভিসিবিলিটি মডিফায়ার আছে - public, private, এবং protected। Public সব জগতে এক্সেস যোগ্য, private কেবল সেই ক্লাসের আত্মস্থ মেথডের সাথে এক্সেস যোগ্য, এবং protected সেই ক্লাসের এবং সাবক্লাসের মেথডের সাথে এক্সেস যোগ্য, তবে সাবক্লাসের বাইরে সরাসরি এক্সেস যোগ্য নয়।


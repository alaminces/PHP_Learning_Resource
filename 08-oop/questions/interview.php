Here are some Object-Oriented Programming (OOP) related interview questions and answers in Bangla:

পিএইচপি এবং অবজেক্ট-অরিয়েন্টেড প্রোগ্রামিং (OOP) কি?

উত্তর: OOP হলো এমন একটি প্রোগ্রামিং প্যারাডাইম যেখানে অবজেক্ট, যা ক্লাসের ইনস্ট্যান্স, ব্যবহার করা হয়, সফটওয়্যার মডেল এবং স্ট্রাকচার করতে ব্যবহৃত হয়।

পিএইচপি-তে ক্লাস কী?

উত্তর: পিএইচপি-তে একটি ক্লাস হলো একটি ব্লুপ্রিন্ট, যা অবজেক্ট তৈরির জন্য ব্যবহৃত হয়। এটি ক্লাসের আবৃত্তি (আট্রিবিউট) এবং মেথড (ফাংশন) সংজ্ঞায়িত করে, যেগুলি ক্লাসের অবজেক্টে থাকবে।

পিএইচপি-তে ক্লাস কীভাবে সংজ্ঞায়িত করা হয়?

উত্তর: পিএইচপি-তে একটি ক্লাস সংজ্ঞায়িত করতে ক্লাস কীওয়ার্ড ব্যবহার করে ক্লাসের নাম এবং একটি ব্রেসের মধ্যে ক্লাসের আট্রিবিউট এবং মেথড সংজ্ঞায়ন করতে হয়।

উদাহরণ:

php
Copy code
class Person {
    // ক্লাসের আট্রিবিউট এবং মেথড সংজ্ঞায়ন করা হয়
    public $name;
    public function sayHello() {
        echo "Hello, " . $this->name;
    }
}
পিএইচপি-তে অবজেক্ট কী?

উত্তর: পিএইচপি-তে অবজেক্ট হলো একটি ক্লাসের ইনস্ট্যান্স, অর্থাৎ ক্লাসের একটি নকশা বা কপি। অবজেক্ট তৈরি করতে হলে new কীওয়ার্ড দিয়ে ক্লাস থেকে একটি ইনস্ট্যান্স তৈরি করতে হয়।

উদাহরণ:

php
Copy code
$person1 = new Person(); // একটি Person ক্লাসের অবজেক্ট তৈরি করা হয়
পিএইচপি-তে এনক্যাপসুলেশন কী?

উত্তর: এনক্যাপসুলেশন হলো ডেটা (আট্রিবিউট) এবং ডেটা নিয়ন্ত্রণ মেথড (মেথড) একত্রিত করার ধারণা, অর্থাৎ একটি ক্লাসের আট্রিবিউট এবং মেথডগুলি সামগ্রিকভাবে একটি একক ইউনিট হিসেবে কাজ করে এবং একটি ক্লাসের বাইরে তাদের অ্যাক্সেস বা পরিবর্তন করা হয় না।

পিএইচপি-তে ইনহেরিটেন্স কী?

উত্তর: ইনহেরিটেন্স হলো একটি মেকানিজম যেখানে একটি নতুন ক্লাস (সাবক্লাস বা ডেরাইভড ক্লাস) একটি বর্তমান ক্লাস (সুপারক্লাস বা বেস ক্লাস) থেকে আট্রিবিউট এবং মেথড ইনহেরিট করতে পারে।

পিএইচপি-তে ইনহেরিটেন্স কীভাবে ইমপ্লিমেন্ট করা হয়?

উত্তর: পিএইচপি-তে ইনহেরিটেন্স ইমপ্লিমেন্ট করতে, ক্লাস সংজ্ঞানার সময় extends কীওয়ার্ড ব্যবহার করে নতুন ক্লাস ডিফাইন করতে হয়।

উদাহরণ:

php
Copy code
class Animal {
    public function makeSound() {
        echo "Some sound";
    }
}

class Dog extends Animal {
    public function makeSound() {
        echo "Bark!";
    }
}
পিএইচপি-তে কনস্ট্রাক্টর কী?

উত্তর: কনস্ট্রাক্টর হলো একটি বিশেষ মেথড যা একটি অবজেক্ট তৈরি হলে স্বয়ংক্রিয়ভাবে কল করা হয়। এটি অবজেক্টের আট্রিবিউট ইনিশিয়ালাইজ করতে ব্যবহৃত হয়।

উদাহরণ:

php
Copy code
class Person {
    public $name;

    // কনস্ট্রাক্টর ডিফাইন করা
    public function __construct($name) {
        $this->name = $name;
    }
}

$person = new Person("John");
পিএইচপি-তে $this কীওয়ার্ড কী কাজ করে?

উত্তর: $this কীওয়ার্ডটি একটি ক্লাসের মেথডের মধ্যে ব্যবহার করা হয় যেটি সম্প্রেষণে সম্প্রেষণ বা স্থিতি পরিবর্তন করতে হলে এই ক্লাসের বর্তমান ইনস্ট্যান্সটির সাথে সংযোগ স্থাপন করে।

পিএইচপি-তে মেথড ওভারলোডিং কী?

উত্তর: পিএইচপি-তে মেথড ওভারলোডিং হলো সাধারণভাবে নতুন প্যারামিটার তালিকার সাথে একই নামে একাধিক মেথড সংজ্ঞায়িত করা। পিএইচপি মেথড ওভারলোডিং সমর্থন করে না।

পিএইচপি-তে মেথড ওভাররাইডিং কী?

উত্তর: মেথড ওভাররাইডিং হলো এমন একটি প্রক্রিয়া যেখানে একটি সাবক্লাস (ডেরাইভড ক্লাস) একটি সুপারক্লাস (বেস ক্লাস) এর মেথডের সাথে একই নামে এবং সাইনেচারের সাথে মেথড সংজ্ঞায়িত করে, সাপেক্ষা করে সুপারক্লাসের মেথডটি প্রতিস্থাপন করে।

পিএইচপি-তে একটি এবস্ট্র্যাক্ট ক্লাস কী?

উত্তর: পিএইচপি-তে একটি এবস্ট্র্যাক্ট ক্লাস হলো একটি ক্লাস যা নিজে নিজেকে তৈরি করা হয় না। এটি একটি ব্লুপ্রিন্ট হয়, যা সাবক্লাস তৈরি করতে ব্যবহৃত হয় এবং এটি আবশ্যকভাবে একটি বা একাধিক এবস্ট্র্যাক্ট মেথড সংজ্ঞায়ন করে যা সাবক্লাসে অবশ্যই ইমপ্লিমেন্ট করতে হয়।

পিএইচপি-তে একটি এবস্ট্র্যাক্ট ক্লাস কীভাবে সংজ্ঞায়িত করা হয়?

উত্তর: পিএইচপি-তে একটি এবস্ট্র্যাক্ট ক্লাস সংজ্ঞায়িত করতে abstract কীওয়ার্ড ব্যবহার করতে হয়। এবং এই ক্লাসের একটি বা একাধিক এবস্ট্র্যাক্ট মেথড সংজ্ঞায়ন করতে হয়, যা সাবক্লাসে অবশ্যই ইমপ্লিমেন্ট করতে হয়।


পিএইচপি-তে ইন্টারফেস কী?

উত্তর: ইন্টারফেস হলো একটি ক্লাসের একটি সমঝোতা, যা আবশ্যকভাবে একটি সেট অব মেথড সংজ্ঞায়িত করে, এবং যে কোন ক্লাস যা এই ইন্টারফেসটি অনুসরণ করতে চাইলে সেই মেথডগুলি ইমপ্লিমেন্ট করতে হবে।

পিএইচপি-তে ইন্টারফেস কীভাবে সংজ্ঞায়িত করা হয়?

উত্তর: পিএইচপি-তে একটি ইন্টারফেস সংজ্ঞায়িত করতে interface কীওয়ার্ড ব্যবহার করতে হয় এবং ইন্টারফেসে একে বা একাধিক মেথড সংজ্ঞায়িত করতে হয়, সেগুলি যা সাবক্লাসে অবশ্যই ইমপ্লিমেন্ট করতে হবে।

